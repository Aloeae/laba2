import random

#Функция для поиска чисел, кратных X
def find_multiples(X, count):
    #Генерация списка случайных чисел в диапазоне от 0 до 200
    numbers = [random.randint(0, 200) for _ in range(count)]

    #Поиск чисел, кратных X, с использованием лямбда-функции и filter
    multiples = list(filter(lambda num: num % X == 0, numbers))

    print("Сгенерированные числа:", numbers)
    
    #Проверка: если кратные числа найдены, вывести их, иначе сообщить об отсутствии
    if multiples:
        print(f"Числа, кратные {X}:", multiples)
    else:
        print(f"Нет чисел, кратных {X}.")

#Функция для получения корректного целого числа от пользователя
def get_valid_input(prompt, min_value=1):

    while True:
        try:
            #Запрашиваем у пользователя ввод с выводом указанного сообщения (prompt)
            value = int(input(prompt))
            
            #Проверяем, что введенное число не меньше минимального значения
            if value >= min_value:
                return value
            else:
                print(f"Число должно быть не меньше {min_value}.")

        #Если ввод не является целым числом, выводим сообщение об ошибке        
        except ValueError:
            print("Некорректный ввод. Пожалуйста, введите целое число.")

# Получаем положительное целое число X с проверкой корректности ввода
X = get_valid_input("Введите положительное целое число X: ", min_value=1)

# Получаем количество случайных чисел, которое должно быть не меньше 10
count = get_valid_input("Введите количество случайных чисел (10 или больше): ", min_value=10)

# Вызов функции для поиска чисел, кратных X, в сгенерированном списке
find_multiples(X, count)
